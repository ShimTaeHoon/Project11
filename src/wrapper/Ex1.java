package wrapper;

public class Ex1 {

	public static void main(String[] args) {
		
		// 1바이트
		Byte bNum = 1;
		
		Short sNum = 1;
		
		// 대부분 int사용
		Integer iNum = 1;
		
		// 8바이트 < 큰타입에 사용
		Long lNum = 1l; // long타입 값을 표현할 때는 'l'을 붙여야함
		
		// 4바이트
		Float fNum = 1.0f; // float타입 값을 표현할 때는 'f'을 붙여야함
		
		// 8바이트, 표현범위 더 정밀
		Double dNum = 1.0;
		
		Character ch = 'a';
		
		Boolean bool = true;
		
	}
	
}
